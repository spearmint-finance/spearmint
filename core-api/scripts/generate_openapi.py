#!/usr/bin/env python3
"""
Generate OpenAPI specification from FastAPI app.

This script exports the OpenAPI spec generated by FastAPI to a JSON file.
The spec can then be used for SDK generation, documentation, or validation.

Usage:
    python generate_openapi.py                    # Output to ../sdk/openapi.json
    python generate_openapi.py path/to/spec.json # Custom output path
"""

import json
import re
import sys
from pathlib import Path

# Add src to path so we can import the app
sys.path.insert(0, str(Path(__file__).parent.parent / "src"))

from financial_analysis.api.main import app


def clean_operation_id(operation_id: str) -> str:
    """
    Transform FastAPI's auto-generated operationId to a clean SDK method name.

    FastAPI generates operationIds like:
        list_accounts_api_accounts_get
        create_transaction_api_transactions_post
        get_transaction_api_transactions__transaction_id__get

    This transforms them to:
        listAccounts
        createTransaction
        getTransaction

    Args:
        operation_id: The original operationId from FastAPI

    Returns:
        Clean camelCase operationId suitable for SDK method names
    """
    # Remove the path suffix (e.g., "_api_accounts_get" or "_api_transactions__transaction_id__delete")
    # Pattern: everything after "_api_" is the path and method
    match = re.match(r'^(.+?)_api_.*$', operation_id)
    if match:
        base_name = match.group(1)
    else:
        # Fallback: just use the original
        base_name = operation_id

    # Convert snake_case to camelCase
    parts = base_name.split('_')
    camel_case = parts[0] + ''.join(word.capitalize() for word in parts[1:])

    return camel_case


def clean_schema_names(spec: dict) -> dict:
    """
    Post-process OpenAPI spec to fix non-PascalCase schema names.

    This handles:
    1. Body_* schemas from file uploads → PascalCase names
    2. Enum-Output suffixes → proper PascalCase
    3. Full module path names → simple names

    Args:
        spec: The OpenAPI specification dictionary

    Returns:
        The spec with cleaned schema names
    """
    if 'components' not in spec or 'schemas' not in spec['components']:
        return spec

    schemas = spec['components']['schemas']
    renames = {}

    # Identify schemas that need renaming
    for schema_name in list(schemas.keys()):
        new_name = None

        # Fix Body_* schemas (e.g., Body_import_transactions_api_import_post)
        if schema_name.startswith('Body_'):
            # Extract the function name part and convert to PascalCase
            # Body_import_transactions_api_import_post -> ImportTransactionsBody
            # Body_suggest_import_profiles_api_import_profiles_suggest_post -> SuggestImportProfilesBody
            match = re.match(r'^Body_(.+?)_api_.*$', schema_name)
            if match:
                func_name = match.group(1)
                # Convert snake_case to PascalCase
                pascal = ''.join(word.capitalize() for word in func_name.split('_'))
                new_name = f"{pascal}Body"

        # Fix Enum-Output suffix (e.g., AnalysisModeEnum-Output)
        elif '-Output' in schema_name:
            new_name = schema_name.replace('-Output', 'Output')

        # Fix full module path names (e.g., financial_analysis__api__schemas__analysis__AnalysisModeEnum)
        elif '__' in schema_name:
            # Extract just the class name at the end
            parts = schema_name.split('__')
            class_name = parts[-1]
            # Add module prefix to avoid conflicts
            module_name = parts[-2] if len(parts) >= 2 else ''
            if module_name:
                new_name = f"{module_name.capitalize()}{class_name}"
            else:
                new_name = class_name

        if new_name and new_name != schema_name:
            renames[schema_name] = new_name

    # Apply renames
    for old_name, new_name in renames.items():
        # Rename the schema
        schemas[new_name] = schemas.pop(old_name)

        # Update all $ref references throughout the spec
        spec = _update_refs(spec, f"#/components/schemas/{old_name}", f"#/components/schemas/{new_name}")

    return spec


def _update_refs(obj, old_ref: str, new_ref: str):
    """Recursively update $ref values in the spec."""
    if isinstance(obj, dict):
        if obj.get('$ref') == old_ref:
            obj['$ref'] = new_ref
        for key, value in obj.items():
            _update_refs(value, old_ref, new_ref)
    elif isinstance(obj, list):
        for item in obj:
            _update_refs(item, old_ref, new_ref)
    return obj


def clean_operation_ids(spec: dict) -> dict:
    """
    Post-process OpenAPI spec to clean up all operationIds.

    This is a workaround until we add explicit operation_id to all FastAPI routes.
    See GitHub issue #10 for the long-term fix.

    Args:
        spec: The OpenAPI specification dictionary

    Returns:
        The spec with cleaned operationIds
    """
    if 'paths' not in spec:
        return spec

    seen_ids = set()

    for path, methods in spec['paths'].items():
        for method, operation in methods.items():
            if isinstance(operation, dict) and 'operationId' in operation:
                original_id = operation['operationId']
                clean_id = clean_operation_id(original_id)

                # Handle duplicates by appending a suffix
                final_id = clean_id
                counter = 2
                while final_id in seen_ids:
                    final_id = f"{clean_id}{counter}"
                    counter += 1

                seen_ids.add(final_id)
                operation['operationId'] = final_id

    return spec


def generate_openapi(output_path: str = None):
    """
    Generate OpenAPI spec from the FastAPI app and save to file.

    Args:
        output_path: Path to save the spec. Defaults to ../sdk/openapi.json

    Returns:
        Path to the generated spec
    """
    if output_path is None:
        # Default: save to sdk/openapi.json (relative to this script)
        output_path = str(Path(__file__).parent.parent.parent / "sdk" / "openapi.json")

    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Get the spec from FastAPI
    spec = app.openapi()

    # Post-process to clean up operationIds for better SDK method names
    spec = clean_operation_ids(spec)

    # Post-process to fix non-PascalCase schema names
    spec = clean_schema_names(spec)

    # Write to file
    with open(output_path, 'w') as f:
        json.dump(spec, f, indent=2)

    # Return path without emoji for cross-platform compatibility
    return output_path


if __name__ == '__main__':
    output = sys.argv[1] if len(sys.argv) > 1 else None
    try:
        generate_openapi(output)
        # Use simple message for cross-platform compatibility
        print("OpenAPI spec generated successfully")
        sys.exit(0)
    except Exception as e:
        print(f"Error generating spec: {e}", file=sys.stderr)
        sys.exit(1)
