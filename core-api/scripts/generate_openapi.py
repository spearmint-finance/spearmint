#!/usr/bin/env python3
"""
Generate OpenAPI specification from FastAPI app.

This script exports the OpenAPI spec generated by FastAPI to a JSON file.
The spec can then be used for SDK generation, documentation, or validation.

Usage:
    python generate_openapi.py                    # Output to ../sdk/openapi.json
    python generate_openapi.py path/to/spec.json # Custom output path
"""

import json
import sys
from pathlib import Path

# Add src to path so we can import the app
sys.path.insert(0, str(Path(__file__).parent.parent / "src"))

from financial_analysis.api.main import app


def generate_openapi(output_path: str = None):
    """
    Generate OpenAPI spec from the FastAPI app and save to file.
    
    Args:
        output_path: Path to save the spec. Defaults to ../sdk/openapi.json
    
    Returns:
        Path to the generated spec
    """
    if output_path is None:
        # Default: save to sdk/openapi.json (relative to this script)
        output_path = str(Path(__file__).parent.parent.parent / "sdk" / "openapi.json")
    
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Get the spec from FastAPI
    spec = app.openapi()
    
    # Write to file
    with open(output_path, 'w') as f:
        json.dump(spec, f, indent=2)
    
    # Return path without emoji for cross-platform compatibility
    return output_path


if __name__ == '__main__':
    output = sys.argv[1] if len(sys.argv) > 1 else None
    try:
        generate_openapi(output)
        # Use simple message for cross-platform compatibility
        print("OpenAPI spec generated successfully")
        sys.exit(0)
    except Exception as e:
        print(f"Error generating spec: {e}", file=sys.stderr)
        sys.exit(1)
