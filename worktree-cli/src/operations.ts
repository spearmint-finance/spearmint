export function removeWorktree(
  name: string,
  options: {
    deleteBranch?: boolean;
    deleteRemote?: boolean;
    keepDatabase?: boolean;
    force?: boolean;
  } = {}
): boolean {
  const projectRoot = findProjectRoot();
  const projectConfig = loadProjectConfig(projectRoot);
  const worktreesDir = join(projectRoot, projectConfig.project.worktree_dir);
  const worktreePath = join(worktreesDir, name);

  if (!existsSync(worktreePath)) {
    logWarning(`Worktree does not exist: ${worktreePath}`);
    return true;
  }

  // Get branch name before removal
  let branchName = '';
  try {
    branchName = getCurrentBranch(worktreePath);
  } catch {
    // Ignore if we can't get branch name
  }

  console.log();
  console.log(chalk.yellow.bold('Removing Worktree'));
  console.log(chalk.dim('─'.repeat(40)));
  console.log(`  Name:   ${name}`);
  console.log(`  Branch: ${branchName || 'unknown'}`);
  console.log(`  Path:   ${worktreePath}`);
  console.log();

  // Check for uncommitted changes
  if (!options.force) {
    try {
      if (hasUncommittedChanges(worktreePath)) {
        logWarning('Worktree has uncommitted changes!');
        log('Use --force to discard changes, or commit them first.');
        return false;
      }
    } catch {
      // Ignore if we can't check status
    }
  }

  // Stop any running dev servers before removing
  const pids = readPidFile(worktreePath);
  if (pids) {
    const backendRunning = pids.backend && isProcessRunning(pids.backend);
    const frontendRunning = pids.frontend && isProcessRunning(pids.frontend);

    if (backendRunning || frontendRunning) {
      logDim('Stopping running dev servers...');
      if (pids.backend && backendRunning) {
        killProcess(pids.backend);
      }
      if (pids.frontend && frontendRunning) {
        killProcess(pids.frontend);
      }
      // Give processes time to terminate
      execSync('sleep 1', { stdio: 'ignore' });
      logSuccess('Dev servers stopped');
    }

    // Clean up PID file
    const pidFile = getPidFile(worktreePath);
    if (existsSync(pidFile)) {
      unlinkSync(pidFile);
    }
  }

  // Remove worktree
  logDim('Removing worktree...');
  gitRemoveWorktree(worktreePath, projectRoot);
  logSuccess('Worktree removed');

  // Remove database
  if (!options.keepDatabase && projectConfig.database) {
    const dbPattern = projectConfig.database.pattern.replace('{name}', name);
    const dbFile = join(projectRoot, dbPattern);
    if (existsSync(dbFile)) {
      unlinkSync(dbFile);
      logSuccess('Database removed');
    }
  }

  // Delete branch
  if (options.deleteBranch && branchName) {
    logDim(`Deleting branch: ${branchName}`);
    deleteBranch(branchName, projectRoot, options.deleteRemote);
    logSuccess('Branch deleted');
  }

  console.log();
  console.log(chalk.green.bold('Cleanup complete!'));
  return true;
}

export function listWorktrees(detailed: boolean = false): void {
  const projectRoot = findProjectRoot();
  const projectConfig = loadProjectConfig(projectRoot);
  const worktreesDir = join(projectRoot, projectConfig.project.worktree_dir);

  console.log();
  console.log(chalk.cyan.bold(`${projectConfig.project.name} Worktrees`));
  console.log(chalk.dim('─'.repeat(40)));

  const worktrees = getExistingWorktrees(projectRoot);
  let wtCount = 0;

  // Normalize paths for comparison
  const normalizedProjectRoot = projectRoot.replace(/\\/g, '/').toLowerCase();

  for (const wt of worktrees) {
    const normalizedWtPath = wt.path.replace(/\\/g, '/').toLowerCase();
    const isMain = normalizedWtPath === normalizedProjectRoot;
    const name = isMain ? null : getWorktreeName(wt.path);

    if (isMain) {
      console.log(`${chalk.bold.white('[MAIN]')} ${wt.branch || 'unknown'}`);
    } else {
      console.log(`${chalk.bold.magenta(`[${name}]`)} ${wt.branch || 'unknown'}`);
      wtCount++;
    }

    console.log(chalk.dim(`       Path: ${wt.path}`));

    if (detailed && !isMain && existsSync(wt.path)) {
      const envPath = join(wt.path, '.env');
      if (existsSync(envPath)) {
        const content = readFileSync(envPath, 'utf-8');
        const apiMatch = content.match(/API_PORT=(\d+)/);
        const viteMatch = content.match(/VITE_PORT=(\d+)/);
        if (apiMatch) console.log(chalk.dim(`       Backend:  http://localhost:${apiMatch[1]}`));
        if (viteMatch) console.log(chalk.dim(`       Frontend: http://localhost:${viteMatch[1]}`));
      }

      try {
        const hasChanges = hasUncommittedChanges(wt.path);
        if (hasChanges) {
          console.log(chalk.yellow('       Status: has uncommitted changes'));
        } else {
          console.log(chalk.dim('       Status: clean'));
        }
      } catch {
        console.log(chalk.dim('       Status: unknown'));
      }
    }

    console.log();
  }

  console.log(`${chalk.bold('Total worktrees:')} ${wtCount}`);

  if (wtCount === 0) {
    console.log();
    console.log(chalk.dim('No worktrees. Create one with:'));
    console.log(chalk.yellow('  worktree create'));
  }
}

export function cleanupAll(force: boolean = false, keepDatabases: boolean = false): void {
  const projectRoot = findProjectRoot();
  const projectConfig = loadProjectConfig(projectRoot);
  const worktreesDir = join(projectRoot, projectConfig.project.worktree_dir);

  const worktrees = getExistingWorktrees(projectRoot);
  let removed = 0;

  // Normalize paths for comparison
  const normalizedProjectRoot = projectRoot.replace(/\\/g, '/').toLowerCase();
  const normalizedWorktreesDir = worktreesDir.replace(/\\/g, '/').toLowerCase();

  for (const wt of worktrees) {
    const normalizedWtPath = wt.path.replace(/\\/g, '/').toLowerCase();

    // Skip main worktree
    if (normalizedWtPath === normalizedProjectRoot) continue;

    // Check if it's in our worktrees directory
    if (normalizedWtPath.startsWith(normalizedWorktreesDir)) {
      const name = getWorktreeName(wt.path);
      logDim(`Removing ${name}...`);
      removeWorktree(name, { force, keepDatabase: keepDatabases });
      removed++;
    }
  }

  if (removed === 0) {
    logWarning('No worktrees to clean up.');
  } else {
    console.log();
    console.log(chalk.green.bold(`Cleaned up ${removed} worktree(s).`));
  }
}

/**
 * Find the real project root (handles being inside a worktree)
 */
function findRealProjectRoot(): { projectRoot: string; isInWorktree: boolean; worktreeName: string | null } {
  const cwd = process.cwd();

  // Check if current directory has a .env with WORKTREE_NAME (most reliable worktree detection)
  const cwdEnvPath = join(cwd, '.env');
  if (existsSync(cwdEnvPath)) {
    const content = readFileSync(cwdEnvPath, 'utf-8');
    const worktreeMatch = content.match(/WORKTREE_NAME=(.+)/);
    if (worktreeMatch) {
      // We're in a worktree - find the main project root
      // Worktrees are typically in <project>/.worktrees/<name>
      const worktreeName = worktreeMatch[1].trim();

      // Walk up to find the real project root (the one with worktree.toml)
      let current = dirname(cwd); // Go up from worktree dir
      while (current !== dirname(current)) {
        if (existsSync(join(current, 'worktree.toml'))) {
          return { projectRoot: current, isInWorktree: true, worktreeName };
        }
        current = dirname(current);
      }

      // Fallback: assume we're two levels deep (.worktrees/wt-xxxx)
      const assumedRoot = dirname(dirname(cwd));
      return { projectRoot: assumedRoot, isInWorktree: true, worktreeName };
    }
  }

  // Not in a worktree - find project root normally
  return { projectRoot: findProjectRoot(), isInWorktree: false, worktreeName: null };
}

/**
 * Detect the current development context (main project or worktree)
 */
export function getDevContext(worktreeName?: string): DevContext {
  const { projectRoot, isInWorktree, worktreeName: detectedWorktreeName } = findRealProjectRoot();
  const projectConfig = loadProjectConfig(projectRoot);
  const cwd = process.cwd();

  // If a worktree name is specified, use that
  if (worktreeName) {
    const worktreePath = join(projectRoot, projectConfig.project.worktree_dir, worktreeName);
    const envPath = join(worktreePath, '.env');

    if (!existsSync(worktreePath)) {
      throw new Error(`Worktree '${worktreeName}' not found`);
    }

    const { backendPort, frontendPort } = readPortsFromEnv(envPath, projectConfig);

    return {
      projectRoot,
      workingDir: worktreePath,
      isWorktree: true,
      worktreeName,
      backendPort,
      frontendPort,
    };
  }

  // If we detected we're in a worktree via .env
  if (isInWorktree && detectedWorktreeName) {
    const worktreePath = join(projectRoot, projectConfig.project.worktree_dir, detectedWorktreeName);
    const envPath = join(worktreePath, '.env');
    const { backendPort, frontendPort } = readPortsFromEnv(envPath, projectConfig);

    return {
      projectRoot,
      workingDir: worktreePath,
      isWorktree: true,
      worktreeName: detectedWorktreeName,
      backendPort,
      frontendPort,
    };
  }

  // Check if we're inside a worktree by path
  const worktreesDir = join(projectRoot, projectConfig.project.worktree_dir);
  const normalizedCwd = cwd.replace(/\\/g, '/').toLowerCase();
  const normalizedWorktreesDir = worktreesDir.replace(/\\/g, '/').toLowerCase();

  if (normalizedCwd.startsWith(normalizedWorktreesDir)) {
    // We're inside a worktree - extract name from path
    const relativePath = cwd.slice(worktreesDir.length + 1);
    const name = relativePath.split(/[/\\]/)[0];
    const worktreePath = join(worktreesDir, name);
    const envPath = join(worktreePath, '.env');

    const { backendPort, frontendPort } = readPortsFromEnv(envPath, projectConfig);

    return {
      projectRoot,
      workingDir: worktreePath,
      isWorktree: true,
      worktreeName: name,
      backendPort,
      frontendPort,
    };
  }

  // We're in the main project
  return {
    projectRoot,
    workingDir: projectRoot,
    isWorktree: false,
    worktreeName: null,
    backendPort: projectConfig.ports.backend_base,
    frontendPort: projectConfig.ports.frontend_base,
  };
}

function readPortsFromEnv(
  envPath: string,
  projectConfig: ReturnType<typeof loadProjectConfig>
): { backendPort: number; frontendPort: number } {
  let backendPort = projectConfig.ports.backend_base;
  let frontendPort = projectConfig.ports.frontend_base;

  if (existsSync(envPath)) {
    const content = readFileSync(envPath, 'utf-8');
    const apiMatch = content.match(/API_PORT=(\d+)/);
    const viteMatch = content.match(/VITE_PORT=(\d+)/);
    if (apiMatch) backendPort = parseInt(apiMatch[1], 10);
    if (viteMatch) frontendPort = parseInt(viteMatch[1], 10);
  }

  return { backendPort, frontendPort };
}

interface PidFile {
  backend?: number;
  frontend?: number;
  startedAt: string;
  context: string;
  backendPort: number;
  frontendPort: number;
}

function getLogsDir(workingDir: string): string {
  return join(workingDir, '.dev-logs');
}

function getPidFile(workingDir: string): string {
  return join(getLogsDir(workingDir), 'pids.json');
}

function readPidFile(workingDir: string): PidFile | null {
  const pidFile = getPidFile(workingDir);
  if (existsSync(pidFile)) {
    try {
      return JSON.parse(readFileSync(pidFile, 'utf-8'));
    } catch {
      return null;
    }
  }
  return null;
}

function writePidFile(workingDir: string, data: PidFile): void {
  const logsDir = getLogsDir(workingDir);
  if (!existsSync(logsDir)) {
    mkdirSync(logsDir, { recursive: true });
  }
  writeFileSync(getPidFile(workingDir), JSON.stringify(data, null, 2));
}

function isProcessRunning(pid: number): boolean {
  try {
    process.kill(pid, 0);
    return true;
  } catch {
    return false;
  }
}

function killProcess(pid: number): boolean {
  const isWindows = process.platform === 'win32';

  try {
    if (isWindows) {
      // On Windows, use taskkill to kill the process tree
      execSync(`taskkill /PID ${pid} /T /F`, { stdio: 'pipe' });
    } else {
      // On Unix, kill the entire process group by using negative PID
      // The process was spawned with detached: true, making it a process group leader
      try {
        process.kill(-pid, 'SIGTERM');
      } catch {
        // Fallback to killing just the PID if group kill fails
        process.kill(pid, 'SIGTERM');
      }
    }
    return true;
  } catch {
    return false;
  }
}

/**
 * Ensure web-app/.env exists with correct VITE_API_URL
 */
function ensureFrontendEnv(workingDir: string, backendPort: number): void {
  const webAppEnvPath = join(workingDir, 'web-app', '.env');
  const expectedUrl = `VITE_API_URL=http://localhost:${backendPort}`;

  if (existsSync(webAppEnvPath)) {
    // Check if VITE_API_URL is correct (no /api suffix)
    const content = readFileSync(webAppEnvPath, 'utf-8');
    if (content.includes(`VITE_API_URL=http://localhost:${backendPort}/api`)) {
      // Fix incorrect URL with /api suffix
      const fixedContent = content.replace(
        /VITE_API_URL=http:\/\/localhost:\d+\/api/,
        expectedUrl
      );
      writeFileSync(webAppEnvPath, fixedContent);
      logSuccess('Fixed web-app/.env (removed /api suffix from VITE_API_URL)');
    } else if (!content.includes('VITE_API_URL=')) {
      // Add VITE_API_URL if missing
      writeFileSync(webAppEnvPath, content + `\n${expectedUrl}\n`);
      logSuccess('Added VITE_API_URL to web-app/.env');
    }
  } else {
    // Create web-app/.env
    writeFileSync(webAppEnvPath, `${expectedUrl}\n`);
    logSuccess('Created web-app/.env with VITE_API_URL');
  }
}

/**
 * Check if database exists and is initialized
 */
function checkDatabase(workingDir: string, worktreeName: string | null): boolean {
  const dbName = worktreeName ? `financial_analysis_${worktreeName}.db` : 'financial_analysis.db';
  // Database is stored in core-api directory
  const dbPath = join(workingDir, 'core-api', dbName);
  return existsSync(dbPath);
}

/**
 * Initialize the database for a worktree
 */
function initializeDatabase(workingDir: string): boolean {
  const isWindows = process.platform === 'win32';
  const coreApiDir = join(workingDir, 'core-api');

  logDim('Initializing database...');

  try {
    execSync(
      isWindows
        ? `cd /d "${coreApiDir}" && ..\\venv\\Scripts\\activate && python -m src.financial_analysis.database.init_db`
        : `cd "${coreApiDir}" && source ../venv/bin/activate 2>/dev/null || source venv/bin/activate && python -m src.financial_analysis.database.init_db`,
      {
        cwd: workingDir,
        stdio: 'pipe',
        shell: isWindows ? 'cmd.exe' : '/bin/bash',
      }
    );
    logSuccess('Database initialized');
    return true;
  } catch (error) {
    logError(`Failed to initialize database: ${error}`);
    return false;
  }
}

/**
 * Start development servers (backend + frontend) as background processes
 */
export function startDev(worktreeName?: string): boolean {
  const context = getDevContext(worktreeName);
  const isWindows = process.platform === 'win32';
  const logsDir = getLogsDir(context.workingDir);

  console.log();
  console.log(chalk.cyan.bold('Starting Development Servers'));
  console.log(chalk.dim('─'.repeat(40)));
  console.log(`  ${chalk.bold('Context:')}  ${context.isWorktree ? chalk.magenta(`[${context.worktreeName}]`) : chalk.white('[MAIN]')}`);
  console.log(`  ${chalk.bold('Path:')}     ${context.workingDir}`);
  console.log(`  ${chalk.bold('Backend:')}  http://localhost:${context.backendPort}`);
  console.log(`  ${chalk.bold('Frontend:')} http://localhost:${context.frontendPort}`);
  console.log();

  // Check if servers are already running
  const existingPids = readPidFile(context.workingDir);
  if (existingPids) {
    const backendRunning = existingPids.backend && isProcessRunning(existingPids.backend);
    const frontendRunning = existingPids.frontend && isProcessRunning(existingPids.frontend);

    if (backendRunning || frontendRunning) {
      logWarning('Servers appear to be already running:');
      if (backendRunning) log(`  Backend PID: ${existingPids.backend}`);
      if (frontendRunning) log(`  Frontend PID: ${existingPids.frontend}`);
      console.log();
      log('Run ' + chalk.cyan('worktree dev --stop') + ' first to stop them.');
      return false;
    } else {
      // PIDs exist but processes aren't running - clean up stale PID file
      logDim('Cleaning up stale PID file...');
      const pidFile = getPidFile(context.workingDir);
      if (existsSync(pidFile)) {
        unlinkSync(pidFile);
      }
    }
  }

  // Ensure web-app/.env exists with correct VITE_API_URL
  ensureFrontendEnv(context.workingDir, context.backendPort);

  // Check and initialize database if needed
  if (!checkDatabase(context.workingDir, context.worktreeName)) {
    log('Database not found. Initializing...');
    if (!initializeDatabase(context.workingDir)) {
      logWarning('Database initialization failed. Server may not work correctly.');
    }
  }

  // Create logs directory
  if (!existsSync(logsDir)) {
    mkdirSync(logsDir, { recursive: true });
  }

  // Create log files
  const backendLogPath = join(logsDir, 'backend.log');
  const frontendLogPath = join(logsDir, 'frontend.log');

  const backendLog = openSync(backendLogPath, 'w');
  const frontendLog = openSync(frontendLogPath, 'w');

  logDim('Starting backend server...');

  // Start backend
  const backendProcess = spawn(
    isWindows ? 'cmd' : 'bash',
    isWindows
      ? ['/c', `cd /d "${join(context.workingDir, 'core-api')}" && ..\\venv\\Scripts\\activate && python -m uvicorn src.financial_analysis.api.main:app --reload --host 0.0.0.0 --port ${context.backendPort}`]
      : ['-c', `cd "${join(context.workingDir, 'core-api')}" && source ../venv/bin/activate 2>/dev/null || source venv/bin/activate && python -m uvicorn src.financial_analysis.api.main:app --reload --host 0.0.0.0 --port ${context.backendPort}`],
    {
      detached: true,
      stdio: ['ignore', backendLog, backendLog],
      cwd: context.workingDir,
    }
  );
  backendProcess.unref();

  logDim('Starting frontend server...');

  // Start frontend
  const frontendProcess = spawn(
    isWindows ? 'cmd' : 'bash',
    isWindows
      ? ['/c', `cd /d "${join(context.workingDir, 'web-app')}" && npm run dev -- --host --port ${context.frontendPort}`]
      : ['-c', `cd "${join(context.workingDir, 'web-app')}" && npm run dev -- --host --port ${context.frontendPort}`],
    {
      detached: true,
      stdio: ['ignore', frontendLog, frontendLog],
      cwd: context.workingDir,
    }
  );
  frontendProcess.unref();

  // Save PIDs
  writePidFile(context.workingDir, {
    backend: backendProcess.pid,
    frontend: frontendProcess.pid,
    startedAt: new Date().toISOString(),
    context: context.isWorktree ? context.worktreeName! : 'main',
    backendPort: context.backendPort,
    frontendPort: context.frontendPort,
  });

  console.log();
  logSuccess('Servers started in background!');
  console.log();
  console.log(chalk.bold('Server URLs:'));
  console.log(`  ${chalk.green('API Docs:')}  http://localhost:${context.backendPort}/api/docs`);
  console.log(`  ${chalk.green('Frontend:')} http://localhost:${context.frontendPort}`);
  console.log();
  console.log(chalk.bold('Logs:'));
  console.log(`  ${chalk.dim('Backend:')}  ${backendLogPath}`);
  console.log(`  ${chalk.dim('Frontend:')} ${frontendLogPath}`);
  console.log();
  console.log(chalk.bold('Commands:'));
  console.log(`  ${chalk.cyan('worktree dev --stop')}   Stop the servers`);
  console.log(`  ${chalk.cyan('worktree dev --logs')}   Tail the log files`);
  console.log(`  ${chalk.cyan('worktree dev --status')} Check server status`);
  console.log();

  return true;
}

/**
 * Stop running development servers
 */
export function stopDev(worktreeName?: string): boolean {
  const context = getDevContext(worktreeName);
  const pids = readPidFile(context.workingDir);

  console.log();
  console.log(chalk.yellow.bold('Stopping Development Servers'));
  console.log(chalk.dim('─'.repeat(40)));

  if (!pids) {
    logWarning('No running servers found (no PID file)');
    return true;
  }

  let stopped = 0;

  if (pids.backend) {
    if (isProcessRunning(pids.backend)) {
      logDim(`Stopping backend (PID ${pids.backend})...`);
      if (killProcess(pids.backend)) {
        logSuccess('Backend stopped');
        stopped++;
      } else {
        logError(`Failed to stop backend (PID ${pids.backend})`);
      }
    } else {
      logDim('Backend was not running');
    }
  }

  if (pids.frontend) {
    if (isProcessRunning(pids.frontend)) {
      logDim(`Stopping frontend (PID ${pids.frontend})...`);
      if (killProcess(pids.frontend)) {
        logSuccess('Frontend stopped');
        stopped++;
      } else {
        logError(`Failed to stop frontend (PID ${pids.frontend})`);
      }
    } else {
      logDim('Frontend was not running');
    }
  }

  // Clean up PID file
  const pidFile = getPidFile(context.workingDir);
  if (existsSync(pidFile)) {
    unlinkSync(pidFile);
  }

  console.log();
  if (stopped > 0) {
    logSuccess(`Stopped ${stopped} server(s)`);
  } else {
    log('No servers were running');
  }
  console.log();

  return true;
}

