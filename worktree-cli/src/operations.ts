function log(message: string): void {
  console.log(message);
}

function logSuccess(message: string): void {
  console.log(chalk.green('✓ ' + message));
}

function logWarning(message: string): void {
  console.log(chalk.yellow('⚠ ' + message));
}

function logError(message: string): void {
  console.log(chalk.red('✗ ' + message));
}

function logDim(message: string): void {
  console.log(chalk.dim(message));
}

/**
 * Add a directory to git's safe.directory config to avoid "unsafe repository" errors
 * This is needed when worktrees are created in directories owned by a different user
 */
function addToGitSafeDirectory(worktreePath: string): void {
  try {
    // Check if already in safe.directory
    const safeDirectories = execSync('git config --global --get-all safe.directory 2>/dev/null || true', {
      encoding: 'utf-8',
    }).trim().split('\n').filter(Boolean);

    if (!safeDirectories.includes(worktreePath)) {
      execSync(`git config --global --add safe.directory "${worktreePath}"`, { stdio: 'pipe' });
      logSuccess('Added worktree to git safe.directory');
    }
  } catch {
    logWarning('Could not add worktree to git safe.directory');
  }
}

export function createWorktree(config: WorktreeConfig, force: boolean = false): boolean {
  const { name, branch, baseBranch, backendPort, frontendPort, projectRoot, worktreePath, projectConfig } = config;

  // Print setup info
  console.log();
  console.log(chalk.cyan.bold('Creating Worktree'));
  console.log(chalk.dim('─'.repeat(40)));
  console.log(`  Name:          ${chalk.white(name)}`);
  console.log(`  Branch:        ${chalk.white(branch)}`);
  console.log(`  Backend Port:  ${chalk.white(backendPort.toString())}`);
  console.log(`  Frontend Port: ${chalk.white(frontendPort.toString())}`);
  console.log(`  Path:          ${chalk.white(worktreePath)}`);
  console.log();

  // Check if worktree already exists
  if (existsSync(worktreePath)) {
    if (force) {
      logWarning('Removing existing worktree...');
      gitRemoveWorktree(worktreePath, projectRoot);
    } else {
      logError(`Worktree already exists: ${worktreePath}`);
      log('Use --force to replace it, or remove it first.');
      return false;
    }
  }

  // Ensure worktrees directory exists
  const worktreesDir = dirname(worktreePath);
  if (!existsSync(worktreesDir)) {
    mkdirSync(worktreesDir, { recursive: true });
  }

  // Fetch latest
  logDim('Fetching latest from remote...');
  try {
    fetchOrigin(projectRoot);
  } catch {
    logWarning('Could not fetch from remote (continuing anyway)');
  }

  // Create worktree
  try {
    gitCreateWorktree(worktreePath, branch, baseBranch, projectRoot);
    logSuccess('Worktree created');
  } catch (error) {
    logError(`Failed to create worktree: ${error}`);
    return false;
  }

  // Add worktree to git safe.directory to avoid "unsafe repository" errors
  addToGitSafeDirectory(worktreePath);

  // Create .env file
  createEnvFile(config);

  // Copy .env.local if exists
  const envLocalSrc = join(projectRoot, '.env.local');
  const envLocalDst = join(worktreePath, '.env.local');
  if (existsSync(envLocalSrc) && !existsSync(envLocalDst)) {
    copyFileSync(envLocalSrc, envLocalDst);
    logSuccess('Copied .env.local');
  }

  // Run post-create hooks
  runPostCreateHooks(config);

  // Print success message
  printSuccessMessage(config);

  return true;
}

function createEnvFile(config: WorktreeConfig): void {
  const { name, backendPort, frontendPort, worktreePath, projectConfig } = config;

  let envContent = `# Worktree configuration for ${name}
# Auto-generated by worktree-cli

# Backend settings
API_PORT=${backendPort}

# Frontend settings
VITE_PORT=${frontendPort}

# Worktree identifier
WORKTREE_NAME=${name}
`;

  // Add database config if specified
  if (projectConfig.database) {
    const dbPattern = projectConfig.database.pattern.replace('{name}', name);
    const dbUrl = projectConfig.database.url_template.replace('{pattern}', dbPattern);
    envContent += `\n# Database\n${projectConfig.database.env_var}=${dbUrl}\n`;
  }

  // Add custom env vars
  if (projectConfig.env) {
    envContent += '\n# Project-specific settings\n';
    for (const [key, value] of Object.entries(projectConfig.env)) {
      // Fix: Remove /api suffix from VITE_API_URL if present (frontend adds /api prefix itself)
      let resolvedValue = value
        .replace('{backend_port}', String(backendPort))
        .replace('{frontend_port}', String(frontendPort))
        .replace('{name}', name);

      // Ensure VITE_API_URL doesn't have /api suffix
      if (key === 'VITE_API_URL' && resolvedValue.endsWith('/api')) {
        resolvedValue = resolvedValue.slice(0, -4);
      }

      envContent += `${key}=${resolvedValue}\n`;
    }
  }

  writeFileSync(join(worktreePath, '.env'), envContent);
  logSuccess('Environment configured (.env)');

  // Create web-app/.env for Vite (it only reads .env from its own directory)
  const webAppDir = join(worktreePath, 'web-app');
  if (existsSync(webAppDir)) {
    const webAppEnvContent = `# Frontend environment for ${name}
# Auto-generated by worktree-cli
VITE_API_URL=http://localhost:${backendPort}
`;
    writeFileSync(join(webAppDir, '.env'), webAppEnvContent);
    logSuccess('Frontend environment configured (web-app/.env)');
  }
}

function runPostCreateHooks(config: WorktreeConfig): void {
  const { worktreePath, projectConfig, projectRoot } = config;
  const isWindows = process.platform === 'win32';

  const hooks = isWindows
    ? projectConfig.hooks?.post_create_windows ?? projectConfig.hooks?.post_create
    : projectConfig.hooks?.post_create;

  if (!hooks || hooks.length === 0) return;

  logDim('Running post-create hooks...');

  let hadChmodError = false;

  for (const hook of hooks) {
    try {
      logDim(`  → ${hook}`);
      const output = execSync(hook, {
        cwd: worktreePath,
        stdio: 'pipe',
        shell: isWindows ? 'cmd.exe' : '/bin/sh',
        encoding: 'utf-8',
      });
      // Print output if any
      if (output && output.trim()) {
        console.log(output);
      }
    } catch (error: unknown) {
      const errorMsg = error instanceof Error ? error.message : String(error);
      const errorOutput = (error as { stderr?: string })?.stderr || '';

      // Detect chmod/permission errors
      if (errorMsg.includes('chmod') || errorMsg.includes('EPERM') ||
          errorOutput.includes('chmod') || errorOutput.includes('Operation not permitted')) {
        hadChmodError = true;
        logWarning(`Hook failed (chmod error): ${hook}`);
      } else {
        logWarning(`Hook failed: ${hook}`);
      }
    }
  }

  // If chmod errors occurred, try to use symlinks as fallback
  if (hadChmodError) {
    logDim('Detected permission errors. Attempting symlink fallback...');
    trySymlinkFallback(worktreePath, projectRoot);
  }

  logSuccess('Post-create hooks completed');
}

/**
 * When chmod errors prevent venv/node_modules from being created properly,
 * fall back to symlinking from the main workspace
 */
function trySymlinkFallback(worktreePath: string, projectRoot: string): void {
  // Try to symlink venv
  const worktreeVenv = join(worktreePath, 'core-api', 'venv');
  const mainVenv = join(projectRoot, 'core-api', 'venv');

  if (existsSync(mainVenv)) {
    try {
      let shouldSymlink = false;

      if (!existsSync(worktreeVenv)) {
        // venv doesn't exist at all
        shouldSymlink = true;
      } else {
        const stat = lstatSync(worktreeVenv);
        if (stat.isSymbolicLink()) {
          // Already a symlink, skip
          shouldSymlink = false;
        } else {
          // Check if venv is broken (no activate script means it's unusable)
          const activatePath = join(worktreeVenv, 'bin', 'activate');
          const hasActivate = existsSync(activatePath);
          // If no activate script, venv is broken
          if (!hasActivate) {
            shouldSymlink = true;
          }
        }
      }

      if (shouldSymlink) {
        if (existsSync(worktreeVenv)) {
          rmSync(worktreeVenv, { recursive: true, force: true });
        }
        symlinkSync(mainVenv, worktreeVenv);
        logSuccess('Linked venv to main workspace (chmod fallback)');
      }
    } catch (error) {
      logWarning(`Could not create venv symlink fallback: ${error}`);
    }
  }

  // Try to symlink node_modules
  const worktreeNodeModules = join(worktreePath, 'web-app', 'node_modules');
  const mainNodeModules = join(projectRoot, 'web-app', 'node_modules');

  if (existsSync(mainNodeModules)) {
    try {
      let shouldSymlink = false;

      if (!existsSync(worktreeNodeModules)) {
        // node_modules doesn't exist at all
        shouldSymlink = true;
      } else {
        const stat = lstatSync(worktreeNodeModules);
        if (stat.isSymbolicLink()) {
          // Already a symlink, skip
          shouldSymlink = false;
        } else {
          // Check if node_modules is broken (no .bin)
          const binPath = join(worktreeNodeModules, '.bin');
          if (!existsSync(binPath)) {
            shouldSymlink = true;
          }
        }
      }

      if (shouldSymlink) {
        if (existsSync(worktreeNodeModules)) {
          rmSync(worktreeNodeModules, { recursive: true, force: true });
        }
        symlinkSync(mainNodeModules, worktreeNodeModules);
        logSuccess('Linked node_modules to main workspace (chmod fallback)');
      }
    } catch (error) {
      logWarning(`Could not create node_modules symlink fallback: ${error}`);
    }
  }
}

